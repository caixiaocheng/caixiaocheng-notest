package com.weiwend.fooldelivery.items;

public class AreaItem {
	
	private String id;   //省、市、区的id
	private String name;   //省、市、区的名称
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
